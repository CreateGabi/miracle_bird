package com.multi.miraclebird.party;

public class PartyImgVO {

	private int partyImgId;
	private String imgName;
	private String oriImgName;
	private int partyId;
	
	public int getPartyImgId() {
		return partyImgId;
	}
	public void setPartyImgId(int partyImgId) {
		this.partyImgId = partyImgId;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public String getOriImgName() {
		return oriImgName;
	}
	public void setOriImgName(String oriImgName) {
		this.oriImgName = oriImgName;
	}
	public int getPartyId() {
		return partyId;
	}
	public void setPartyId(int partyId) {
		this.partyId = partyId;
	}
	
}
